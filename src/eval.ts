import ollama from "ollama";
import { z } from "zod";
import { getCombinedGoldenDataset } from "./evals/combined-golden-dataset.js";
import { modelConfigs, ModelConfigKey, shared } from "./config.js";
import { compareGolden, printGoldenResults } from "./evals/golden.js";
import { runStructuralHeuristics, logHeuristicResults } from "./evals/structural.js";
import { writeEvalReport } from "./evals/report-writer.js";
import { EvaluatedJob, jobEvaluationSchema, JobStatus } from "./types/evaluated-job.js";
import { BaseJob } from "./types/base.js";
import { unifiedFilterPrompt } from "./pipeline/prompts.js";

const modelArg = process.argv[2] as ModelConfigKey | undefined;
const failedOnlyArg = process.argv.includes("--failed-only");

if (!modelArg || !(modelArg in modelConfigs)) {
    const available = Object.keys(modelConfigs).join(", ");
    console.error(`Usage: pnpm eval <model>\nAvailable models: ${available}`);
    process.exit(1);
}

const validModelKey = modelArg;
const modelConfig = modelConfigs[validModelKey];

async function main() {
    // Extract just the jobs from the golden dataset (no labels)
    const jobs: BaseJob[] = getCombinedGoldenDataset().map((entry) => entry.job);

    console.log(`🧪 Running golden dataset eval with ${jobs.length} jobs...`);
    console.log(`   Model: ${modelConfig.model}`);
    console.log(`   Temperature: ${modelConfig.temperature}\n`);

    // Run filter through ollama (same pipeline as evaluate.ts, but no crawling)
    const filterPrompt = unifiedFilterPrompt.replace(
        "{{jobs}}",
        JSON.stringify(jobs, null, 2),
    );

    console.log("🤖 Sending jobs to LLM...");
    const response = await ollama.chat({
        model: modelConfig.model,
        keep_alive: shared.keepAlive,
        think: modelConfig.think,
        messages: [{ role: "user", content: filterPrompt }],
        format: z.toJSONSchema(jobEvaluationSchema),
        options: { temperature: modelConfig.temperature },
    });

    // how long the model took to load into memory. If this number is very high, Ollama had to pull the model from your hard drive into your RAM/VRAM.
    console.log(`⏱️  Load Duration: ${response.load_duration / 1_000_000_000} s`);

    // time spent reading and understanding your input prompt
    console.log(`⏱️  Prompt Evaluation Duration: ${response.prompt_eval_duration / 1_000_000_000} s`);

    // time spent creating the new text response
    console.log(`⏱️  Eval Duration: ${response.eval_duration / 1_000_000_000} s`);

    // total output tokens generated by the model
    console.log(`📝 Output Tokens: ${response.eval_count}`);

    // total time for the whole request
    console.log(`⏱️  Total Duration: ${response.total_duration / 1_000_000_000} s\n`);

    // total input tokens in your prompt
    console.log(`📝 Input Tokens: ${response.prompt_eval_count}`);

    // total output tokens generated by the model
    console.log(`📝 Output Tokens: ${response.eval_count}`);

    // Parse LLM output and merge with original jobs by jobURL
    let parsed: ReturnType<typeof jobEvaluationSchema.parse>;
    try {
        parsed = jobEvaluationSchema.parse(JSON.parse(response.message.content.replaceAll('```', '')));
    } catch (err) {
        console.warn("⚠️  Failed to parse LLM output:");
        console.warn(err);
        console.warn("Raw LLM response:");
        console.warn(response.message.content);
        process.exit(1);
    }

    const jobByUrl = new Map<string, BaseJob>();
    for (const job of jobs) {
        jobByUrl.set(job.jobURL, job);
    }

    const inputUrls = new Set(jobs.map((j) => j.jobURL));
    const outputUrls = new Set<string>();
    const aiOutput: EvaluatedJob<BaseJob>[] = [];

    for (const item of parsed as Array<Record<string, unknown>>) {
        const { jobURL, status, reason, experienceLevel, skills } = item as {
            jobURL: string;
            status: JobStatus;
            reason: string[];
            experienceLevel: string;
            skills: string[];
        };

        if (!inputUrls.has(jobURL)) {
            console.warn(`⚠️  LLM returned unknown jobURL not in input, skipping: ${jobURL}`);
            continue;
        }
        if (outputUrls.has(jobURL)) {
            console.warn(`⚠️  LLM returned duplicate jobURL, skipping: ${jobURL}`);
            continue;
        }

        const job = jobByUrl.get(jobURL);
        if (!job) {
            console.warn(`⚠️  jobURL lookup failed (should not happen), skipping: ${jobURL}`);
            continue;
        }

        outputUrls.add(jobURL);
        aiOutput.push({ job, status, reason, experienceLevel, skills });
    }

    if (outputUrls.size !== inputUrls.size) {
        const missing = [...inputUrls].filter((url) => !outputUrls.has(url));
        console.warn(
            `⚠️  LLM dropped ${missing.length} job(s) from output: ${missing.join(", ")}`,
        );
    }

    console.log(`✅ Received ${aiOutput.length} evaluated jobs\n`);

    // Run structural heuristics
    console.log("── Structural Heuristics ──\n");
    const heuristicResults = runStructuralHeuristics(jobs, aiOutput);
    logHeuristicResults(heuristicResults);

    // Run golden comparison
    const comparison = compareGolden(getCombinedGoldenDataset(), aiOutput);
    printGoldenResults(comparison, failedOnlyArg);

    // Write report to eval-results/
    const reportPath = writeEvalReport({
        modelKey: validModelKey,
        modelConfig,
        comparison,
        heuristics: heuristicResults,
        goldenDataset: getCombinedGoldenDataset(),
    });
    console.log(`\n📄 Report saved: ${reportPath}`);

    // Exit with code based on accuracy threshold
    const threshold = 0.8;
    if (comparison.overallAccuracy >= threshold) {
        console.log(`\n✅ PASSED: Accuracy ${(comparison.overallAccuracy * 100).toFixed(1)}% meets threshold ${(threshold * 100).toFixed(0)}%`);
    } else {
        console.log(`\n❌ FAILED: Accuracy ${(comparison.overallAccuracy * 100).toFixed(1)}% below threshold ${(threshold * 100).toFixed(0)}%`);
        process.exit(1);
    }
}

main().catch((err) => {
    console.error("Eval failed:", err);
    process.exit(1);
});
